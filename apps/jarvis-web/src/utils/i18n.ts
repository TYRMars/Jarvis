// String table + lookup. `messages.{en,zh}` are typed loosely as
// `Record<string, MessageValue>` so the wide variety of message
// shapes — plain strings and pluralisation/interpolation closures
// of various arities — share one table. `t()` is the universal
// accessor: looks up the key under the active language with an
// `en` fallback, and invokes function-shaped values with the
// trailing args so `t("msgCount", 3)` returns "3 msg" / "3 条".

